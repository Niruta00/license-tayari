Answer: ब्याक गियार


4) कुन सबारीले बाटो इजाजत पत्र लिनु पर्छ?
Answer: सार्बजनिक


5) ट्रली बस के ले चल्छ ?
Answer: विधूत


6) नेपालमा सडकमा कुन साइटबाट चलाउनुपर्छ ?
Answer: बायाँ बाट


