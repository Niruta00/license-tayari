2) तीव्र गतिको गाडी के गर्दा बडी खतरा हुन्छ ?
Answer: एक्कासि मोड्दा


3) संसारमा सबै भन्दा पैला ट्राफिक लाइटको कुन देशमा सुरु भएको हो ?
Answer: बेलायत


