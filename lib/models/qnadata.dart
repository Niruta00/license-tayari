Answer: माथिका सबै


8) खारेज भएको चालकको अनुमतिपत्र कति दिनभित्र फिर्ता बुझाउनुपर्छ?
Answer: ७


9) कुन सवारीमा ब्रेक आयाल आवश्यकता पर्दैन?
Answer: हाइड्रोलिक ब्रेक भयको


10) सार्वजनिक सबरिभित्र कसले धूम्रपान गर्न पाउँछ?
Answer: कसैले पनि पाउँदैन


11) इन्जिनलाई चिस्याउन पानी राख्ने उपकरलाई के भनिन्छ?
Answer: रेडियटर


12) झुठा नुम्बर प्लेट राखी सवारी चलाएमा के सजाय हुन्छ?
Answer: माथिका सबै हुन सक्छा


13) नेपाल अधिराज्य भरमा यातायात कार्यालय कति अंचलमा छन्?
Answer: १३


14) एक तर्फी सडकमा कस्तो अवस्थामा बदी दुर्घटना हुन्छन्?
Answer: अचानक लेन बदल्दा


15) मोटरसाइकलको लिखित परीक्षामा सम्मिलित हुन दरखास्थ दस्तुर वापर कति रकम लाग्छ?
Answer: रू ५००


16) दुर्घटनाको घाइतेलाई अक्सिजन कति समयसम्म दिनुपर्छ?
Answer: होस् नआएसम्म


17) क्लचले कहाँ काम गर्छ?
Answer: इन्जिनमा र गियर बक्समा


18) ब्रेक कुन आवस्थामा निष्कृय हुन सक्छ?
Answer: ओरालोमा न्यूट्रलमा चलाउँदा


19) सवारी साधन मध्ये सबैभन्दा असुरक्षित कुन हो?
Answer: मोटरसाइकल


